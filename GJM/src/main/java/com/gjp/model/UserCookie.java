package com.gjp.model;

/**
 * 用户登录cookie实体
 * @author zoe
 *
 */
public class UserCookie {

	//员工编号
	private Integer em_id;
	//员工姓名
	private String em_name;
	//内部人员电话
	private String em_phone;
	//职位名称
	private String ucr_name;
	//部门名称
	private String ucc_name;
	//部门名称
	private String em_account;
	
	public UserCookie() {
		super();
	}

	public Integer getEm_id() {
		return em_id;
	}

	public void setEm_id(Integer em_id) {
		this.em_id = em_id;
	}

	public String getEm_name() {
		return em_name;
	}

	public void setEm_name(String em_name) {
		this.em_name = em_name;
	}

	public String getEm_phone() {
		return em_phone;
	}

	public void setEm_phone(String em_phone) {
		this.em_phone = em_phone;
	}

	public String getUcr_name() {
		return ucr_name;
	}

	public void setUcr_name(String ucr_name) {
		this.ucr_name = ucr_name;
	}

	public String getUcc_name() {
		return ucc_name;
	}

	public void setUcc_name(String ucc_name) {
		this.ucc_name = ucc_name;
	}

	public String getEm_account() {
		return em_account;
	}

	public void setEm_account(String em_account) {
		this.em_account = em_account;
	}
	
	
}
