package com.gjp.model;

import java.util.List;

/**
 * 在线预约界面配置
 * @author hu
 *
 */
public class HouseBookConfig {
	
	// ID
	private Integer bc_id;
	// 父ID
	private Integer bc_pid;
	// 是否为父节点 0-否；1-是
	private String isFather;
	// 字段中文名
	private String fieldNameCn;
	// 字段名称
	private String fieldNameEn;
	// 控件类型
	private String elementType;
	// 默认值
	private String defaultValue;
	// 字段值
	private String fieldValue;
	// placeholder
	private String placeholder;
	// 非空
	private String isNull;
	// 支持uft-8
	private String isUtf8;
	// 校验规则
	private String regRule;
	
	//扩展
	private List<String> bc_idsList;
	public Integer getBc_id() {
		return bc_id;
	}
	public void setBc_id(Integer bc_id) {
		this.bc_id = bc_id;
	}
	public Integer getBc_pid() {
		return bc_pid;
	}
	public void setBc_pid(Integer bc_pid) {
		this.bc_pid = bc_pid;
	}
	public String getIsFather() {
		return isFather;
	}
	public void setIsFather(String isFather) {
		this.isFather = isFather;
	}
	public String getFieldNameCn() {
		return fieldNameCn;
	}
	public void setFieldNameCn(String fieldNameCn) {
		this.fieldNameCn = fieldNameCn;
	}
	public String getFieldNameEn() {
		return fieldNameEn;
	}
	public void setFieldNameEn(String fieldNameEn) {
		this.fieldNameEn = fieldNameEn;
	}
	public String getElementType() {
		return elementType;
	}
	public void setElementType(String elementType) {
		this.elementType = elementType;
	}
	public String getDefaultValue() {
		return defaultValue;
	}
	public void setDefaultValue(String defaultValue) {
		this.defaultValue = defaultValue;
	}
	public String getFieldValue() {
		return fieldValue;
	}
	public void setFieldValue(String fieldValue) {
		this.fieldValue = fieldValue;
	}
	public String getPlaceholder() {
		return placeholder;
	}
	public void setPlaceholder(String placeholder) {
		this.placeholder = placeholder;
	}
	public String getIsNull() {
		return isNull;
	}
	public void setIsNull(String isNull) {
		this.isNull = isNull;
	}
	public String getIsUtf8() {
		return isUtf8;
	}
	public void setIsUtf8(String isUtf8) {
		this.isUtf8 = isUtf8;
	}
	public String getRegRule() {
		return regRule;
	}
	public void setRegRule(String regRule) {
		this.regRule = regRule;
	}
	public List<String> getBc_idsList() {
		return bc_idsList;
	}
	public void setBc_idsList(List<String> bc_idsList) {
		this.bc_idsList = bc_idsList;
	}
	@Override
	public String toString() {
		return "HouseBookConfig [bc_id=" + bc_id + ", bc_pid=" + bc_pid + ", fieldNameCn=" + fieldNameCn
				+ ", fieldNameEn=" + fieldNameEn + ", elementType=" + elementType + ", defaultValue=" + defaultValue
				+ ", fieldValue=" + fieldValue + ", placeholder=" + placeholder + ", isNull=" + isNull + ", isUtf8="
				+ isUtf8 + ", regRule=" + regRule + "]";
	}
	
}
