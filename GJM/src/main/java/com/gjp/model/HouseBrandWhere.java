package com.gjp.model;

import java.util.Date;

/**
 * 房屋品牌条件
 * 
 * @author zoe
 *
 */
public class HouseBrandWhere {

	// 品牌条件编码
	private Integer bw_id;
	// 品牌条件名
	private String bw_name;
	// 房屋品牌编码
	private Integer hb_id;
	// 品牌条件时间
	private Date bw_time;

	public HouseBrandWhere() {
		super();
	}

	public Integer getHb_id() {
		return hb_id;
	}

	public void setHb_id(Integer hb_id) {
		this.hb_id = hb_id;
	}

	public Integer getBw_id() {
		return bw_id;
	}

	public void setBw_id(Integer bw_id) {
		this.bw_id = bw_id;
	}

	public String getBw_name() {
		return bw_name;
	}

	public void setBw_name(String bw_name) {
		this.bw_name = bw_name;
	}

	public Date getBw_time() {
		return bw_time;
	}

	public void setBw_time(Date bw_time) {
		this.bw_time = bw_time;
	}

	
}
