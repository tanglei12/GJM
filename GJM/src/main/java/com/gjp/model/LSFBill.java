package com.gjp.model;

import java.util.Date;

/**
 * 乐首付账单
 * 
 * @author 陈智颖
 *
 * @version 创建时间：2015年8月12日 下午5:45:21 
 */
public class LSFBill {
	//乐首付账单ID
	private Integer lsfb_id;
	//乐首付订单号
	private String lsfb_orderCode;
	//乐首付姓名
	private String lsfb_realName;
	//乐首付手机
	private String lsfb_phone;
	//乐首付账单状态
	private String lsfb_billStatus;
	//乐首付期数
	private Integer lsfb_cycleNum;
	//乐首付金额
	private Double lsfb_money;
	//乐首付支付金额
	private Double lsfb_payAmoun;
	//乐首付还款时间
	private Date lsfb_repayment;
	//乐首付支付时间
	private Date lsfb_payDate;
	//乐首付支付方式
	private String lsfb_payType;
	//数量
	private Integer size;
	
	public LSFBill(){
		
	}

	public Integer getLsfb_id() {
		return lsfb_id;
	}

	public void setLsfb_id(Integer lsfb_id) {
		this.lsfb_id = lsfb_id;
	}

	public String getLsfb_orderCode() {
		return lsfb_orderCode;
	}

	public void setLsfb_orderCode(String lsfb_orderCode) {
		this.lsfb_orderCode = lsfb_orderCode;
	}

	public String getLsfb_realName() {
		return lsfb_realName;
	}

	public void setLsfb_realName(String lsfb_realName) {
		this.lsfb_realName = lsfb_realName;
	}

	public String getLsfb_phone() {
		return lsfb_phone;
	}

	public void setLsfb_phone(String lsfb_phone) {
		this.lsfb_phone = lsfb_phone;
	}

	public String getLsfb_billStatus() {
		return lsfb_billStatus;
	}

	public void setLsfb_billStatus(String lsfb_billStatus) {
		this.lsfb_billStatus = lsfb_billStatus;
	}

	public Integer getLsfb_cycleNum() {
		return lsfb_cycleNum;
	}

	public void setLsfb_cycleNum(Integer lsfb_cycleNum) {
		this.lsfb_cycleNum = lsfb_cycleNum;
	}

	public Double getLsfb_money() {
		return lsfb_money;
	}

	public void setLsfb_money(Double lsfb_money) {
		this.lsfb_money = lsfb_money;
	}

	public Double getLsfb_payAmoun() {
		return lsfb_payAmoun;
	}

	public void setLsfb_payAmoun(Double lsfb_payAmoun) {
		this.lsfb_payAmoun = lsfb_payAmoun;
	}

	public Date getLsfb_repayment() {
		return lsfb_repayment;
	}

	public void setLsfb_repayment(Date lsfb_repayment) {
		this.lsfb_repayment = lsfb_repayment;
	}

	public Date getLsfb_payDate() {
		return lsfb_payDate;
	}

	public void setLsfb_payDate(Date lsfb_payDate) {
		this.lsfb_payDate = lsfb_payDate;
	}

	public String getLsfb_payType() {
		return lsfb_payType;
	}

	public void setLsfb_payType(String lsfb_payType) {
		this.lsfb_payType = lsfb_payType;
	}

	public Integer getSize() {
		return size;
	}

	public void setSize(Integer size) {
		this.size = size;
	}

	@Override
	public String toString() {
		return "LSFBill [lsfb_id=" + lsfb_id + ", lsfb_orderCode="
				+ lsfb_orderCode + ", lsfb_realName=" + lsfb_realName
				+ ", lsfb_phone=" + lsfb_phone + ", lsfb_billStatus="
				+ lsfb_billStatus + ", lsfb_cycleNum=" + lsfb_cycleNum
				+ ", lsfb_money=" + lsfb_money + ", lsfb_payAmoun="
				+ lsfb_payAmoun + ", lsfb_repayment=" + lsfb_repayment
				+ ", lsfb_payDate=" + lsfb_payDate + ", lsfb_payType="
				+ lsfb_payType + ", size=" + size + "]";
	}

}
