package com.gjp.model;

/**
 * 意向房源图片类型
 * 
 * @author zoe
 *
 */
public class HouseIntentionImageType {

	// 房屋图片类型编号
	private Integer hint_id;
	// 图片类型 (effect 效果图片,solid 户型图片,3d 3D效果图)
	private String hint_type;
	// 房屋编号
	private Integer phi_id;
	// 房屋图片
	private Integer him_id;
	// 房源类型
	private String hint_str;
	// 库存房屋编号
	private Integer hi_id;

	public HouseIntentionImageType() {
		super();
	}

	public Integer getHint_id() {
		return hint_id;
	}

	public void setHint_id(Integer hint_id) {
		this.hint_id = hint_id;
	}

	public String getHint_type() {
		return hint_type;
	}

	public void setHint_type(String hint_type) {
		this.hint_type = hint_type;
	}

	public Integer getPhi_id() {
		return phi_id;
	}

	public void setPhi_id(Integer phi_id) {
		this.phi_id = phi_id;
	}

	public Integer getHim_id() {
		return him_id;
	}

	public void setHim_id(Integer him_id) {
		this.him_id = him_id;
	}

	public String getHint_str() {
		return hint_str;
	}

	public void setHint_str(String hint_str) {
		this.hint_str = hint_str;
	}

	public Integer getHi_id() {
		return hi_id;
	}

	public void setHi_id(Integer hi_id) {
		this.hi_id = hi_id;
	}

	@Override
	public String toString() {
		return "HouseIntentionImageType [hint_id=" + hint_id + ", hint_type=" + hint_type + ", phi_id=" + phi_id + ", him_id=" + him_id + ", hint_str=" + hint_str + ", hi_id="
				+ hi_id + "]";
	}

}
