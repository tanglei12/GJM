package com.gjp.model;

import java.util.Date;

/**
 * @author 陈智颖
 * @create 2017-08-30 2:32 PM
 **/
public class CompanyFund {

    // 门店基金编码
    private Integer ccf_id;
    // 1:收 2：付
    private Integer ccf_pay;
    // 费用来源
    private String ccf_source;
    // 基金使用金额
    private Double ccf_money;
    // 基金结余
    private Double ccf_balanceMoney;
    // 基金时间
    private Date ccf_date;
    // 部门编码
    private Integer ucc_id;

    public Integer getCcf_id() {
        return ccf_id;
    }

    public void setCcf_id(Integer ccf_id) {
        this.ccf_id = ccf_id;
    }

    public Integer getCcf_pay() {
        return ccf_pay;
    }

    public void setCcf_pay(Integer ccf_pay) {
        this.ccf_pay = ccf_pay;
    }

    public String getCcf_source() {
        return ccf_source;
    }

    public void setCcf_source(String ccf_source) {
        this.ccf_source = ccf_source;
    }

    public Double getCcf_money() {
        return ccf_money;
    }

    public void setCcf_money(Double ccf_money) {
        this.ccf_money = ccf_money;
    }

    public Double getCcf_balanceMoney() {
        return ccf_balanceMoney;
    }

    public void setCcf_balanceMoney(Double ccf_balanceMoney) {
        this.ccf_balanceMoney = ccf_balanceMoney;
    }

    public Date getCcf_date() {
        return ccf_date;
    }

    public void setCcf_date(Date ccf_date) {
        this.ccf_date = ccf_date;
    }

    public Integer getUcc_id() {
        return ucc_id;
    }

    public void setUcc_id(Integer ucc_id) {
        this.ucc_id = ucc_id;
    }
}
