package com.gjp.model;

import java.util.Date;

/**
 * 物品存放记录
 * 
 * @author 秦莎
 *
 * @version 创建时间：2016年8月10日 上午14:52:45
 */
public class ItemsStorageRecord {

	// 物品存放记录ID
	private Integer isr_id;
	// 物品库唯一编号
	private String inv_code;
	// 记录内容
	private String isr_content;
	// 办理人
	private Integer em_id;
	// 记录时间
	private Date isr_createTime;
	// 是否处理（0：未处理、1：已处理）（默认已处理）
	private Integer isr_isHandle;
	// 搬离位置（存房屋编号、公司为0）
	private String isr_positionBefore;
	// 存放位置（存房屋编号、公司为0）
	private String isr_positionAfter;
	
	
	public Integer getIsr_id() {
		return isr_id;
	}
	public void setIsr_id(Integer isr_id) {
		this.isr_id = isr_id;
	}
	public String getInv_code() {
		return inv_code;
	}
	public void setInv_code(String inv_code) {
		this.inv_code = inv_code;
	}
	public String getIsr_content() {
		return isr_content;
	}
	public void setIsr_content(String isr_content) {
		this.isr_content = isr_content;
	}
	public Integer getEm_id() {
		return em_id;
	}
	public void setEm_id(Integer em_id) {
		this.em_id = em_id;
	}
	public Date getIsr_createTime() {
		return isr_createTime;
	}
	public void setIsr_createTime(Date isr_createTime) {
		this.isr_createTime = isr_createTime;
	}
	public Integer getIsr_isHandle() {
		return isr_isHandle;
	}
	public void setIsr_isHandle(Integer isr_isHandle) {
		this.isr_isHandle = isr_isHandle;
	}
	public String getIsr_positionBefore() {
		return isr_positionBefore;
	}
	public void setIsr_positionBefore(String isr_positionBefore) {
		this.isr_positionBefore = isr_positionBefore;
	}
	public String getIsr_positionAfter() {
		return isr_positionAfter;
	}
	public void setIsr_positionAfter(String isr_positionAfter) {
		this.isr_positionAfter = isr_positionAfter;
	}

	
}
