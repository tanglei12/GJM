package com.gjp.model;

import java.util.Date;

/**
 * 热点问题
 * 
 * @author zoe
 *
 */
public class HotspotIssuesProblem {

	// 热点问题编码
	private Integer sip_id;
	// 热点问题名
	private String sip_name;
	// 热点问题答案
	private String sip_content;
	// 上传者
	private String sip_people;
	// 上传时间
	private Date sip_time;
	// 服务类型编码
	private Integer st_id;
	
	//扩展字段
	// 服务类型名
	private String st_name;

	public HotspotIssuesProblem() {
		super();
	}

	public Integer getSip_id() {
		return sip_id;
	}

	public void setSip_id(Integer sip_id) {
		this.sip_id = sip_id;
	}

	public String getSip_name() {
		return sip_name;
	}

	public void setSip_name(String sip_name) {
		this.sip_name = sip_name;
	}

	public String getSip_content() {
		return sip_content;
	}

	public void setSip_content(String sip_content) {
		this.sip_content = sip_content;
	}

	public String getSip_people() {
		return sip_people;
	}

	public void setSip_people(String sip_people) {
		this.sip_people = sip_people;
	}

	public Date getSip_time() {
		return sip_time;
	}

	public void setSip_time(Date sip_time) {
		this.sip_time = sip_time;
	}

	public Integer getSt_id() {
		return st_id;
	}

	public void setSt_id(Integer st_id) {
		this.st_id = st_id;
	}

	public String getSt_name() {
		return st_name;
	}

	public void setSt_name(String st_name) {
		this.st_name = st_name;
	}


}
