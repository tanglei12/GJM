package com.gjp.model;

import java.util.Date;

/**
 * 
 * 房屋状态
 * 
 * @author 陈智颖
 *
 * @version 创建时间：2016年7月1日 下午2:33:56 
 */
public class HouseInformationState {

	// 房屋状态编码
	private Integer his_id;
	// 房屋类型名
	private String his_name;
	// 父级
	private Integer his_pid;
	// 超父级
	private Integer his_spid;
	// 创建时间
	private Date his_date;
	
	public HouseInformationState(){
		
	}
	public Integer getHis_id() {
		return his_id;
	}

	public void setHis_id(Integer his_id) {
		this.his_id = his_id;
	}

	public String getHis_name() {
		return his_name;
	}

	public void setHis_name(String his_name) {
		this.his_name = his_name;
	}

	public Integer getHis_pid() {
		return his_pid;
	}

	public void setHis_pid(Integer his_pid) {
		this.his_pid = his_pid;
	}

	public Integer getHis_spid() {
		return his_spid;
	}

	public void setHis_spid(Integer his_spid) {
		this.his_spid = his_spid;
	}

	public Date getHis_date() {
		return his_date;
	}

	public void setHis_date(Date his_date) {
		this.his_date = his_date;
	}
	
}
