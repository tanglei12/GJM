package com.gjp.model;

import java.util.Date;

/**
 * @author 陈智颖
 *
 * @version 创建时间：2015年6月27日 下午6:13:48
 */
public class HouseImageType {

	// 房屋图片类型编号
	private Integer hint_id;
	// 图片类型 (effect 效果图片,solid 户型图片,3d 3D效果图)
	private String hint_type;
	// 房屋编号
	private Integer phi_id;
	// 房屋图片
	private Integer him_id;
	// 房屋图片名称
	private String him_name;
	// 房屋图片路径
	private String him_path;
	// 图片上传时间
	private Date him_time;
	//房屋编号
	private Integer hi_id;

	public HouseImageType() {

	}

	
	public Integer getHi_id() {
		return hi_id;
	}

	public void setHi_id(Integer hi_id) {
		this.hi_id = hi_id;
	}


	public Integer getHint_id() {
		return hint_id;
	}

	public void setHint_id(Integer hint_id) {
		this.hint_id = hint_id;
	}

	public String getHint_type() {
		return hint_type;
	}

	public void setHint_type(String hint_type) {
		this.hint_type = hint_type;
	}

	public Integer getPhi_id() {
		return phi_id;
	}

	public void setPhi_id(Integer phi_id) {
		this.phi_id = phi_id;
	}

	public Integer getHim_id() {
		return him_id;
	}

	public void setHim_id(Integer him_id) {
		this.him_id = him_id;
	}

	public String getHim_name() {
		return him_name;
	}

	public void setHim_name(String him_name) {
		this.him_name = him_name;
	}

	public String getHim_path() {
		return him_path;
	}

	public void setHim_path(String him_path) {
		this.him_path = him_path;
	}

	public Date getHim_time() {
		return him_time;
	}

	public void setHim_time(Date him_time) {
		this.him_time = him_time;
	}

	@Override
	public String toString() {
		return "HouseImageType [hint_id=" + hint_id + ", hint_type="
				+ hint_type + ", phi_id=" + phi_id + ", him_id=" + him_id
				+ ", him_name=" + him_name + ", him_path=" + him_path
				+ ", him_time=" + him_time + "]";
	}

	
	
}
