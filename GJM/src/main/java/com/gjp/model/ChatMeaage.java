package com.gjp.model;

import java.util.Date;

/**
 * 聊天应用类
 * 
 * @author 陈智颖
 *
 * @version 创建时间：2016年1月7日 下午3:11:54 
 */
public class ChatMeaage {

	//聊天应用编码
	private Integer cm_id;
	//聊天发送者
	private String cm_sendAccount;
	//聊天信息
	private String cm_content;
	//发送人
	private String cm_receiveAccount;
	//发送时间
	private Date cm_timer;
	//还需要发送的人
	private String offlinePerson;
	//部门
	private String department;
	//姓名
	private String name;
	//开始查询的数据
	private Integer start;
	//历史记录条数
	private Integer size;
	
	public Integer getCm_id() {
		return cm_id;
	}
	public void setCm_id(Integer cm_id) {
		this.cm_id = cm_id;
	}
	public String getCm_sendAccount() {
		return cm_sendAccount;
	}
	public void setCm_sendAccount(String cm_sendAccount) {
		this.cm_sendAccount = cm_sendAccount;
	}
	public String getCm_content() {
		return cm_content;
	}
	public void setCm_content(String cm_content) {
		this.cm_content = cm_content;
	}
	public String getCm_receiveAccount() {
		return cm_receiveAccount;
	}
	public void setCm_receiveAccount(String cm_receiveAccount) {
		this.cm_receiveAccount = cm_receiveAccount;
	}
	public Date getCm_timer() {
		return cm_timer;
	}
	public void setCm_timer(Date cm_timer) {
		this.cm_timer = cm_timer;
	}
	public String getOfflinePerson() {
		return offlinePerson;
	}
	public void setOfflinePerson(String offlinePerson) {
		this.offlinePerson = offlinePerson;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getStart() {
		return start;
	}
	public void setStart(Integer start) {
		this.start = start;
	}
	public Integer getSize() {
		return size;
	}
	public void setSize(Integer size) {
		this.size = size;
	}
}
