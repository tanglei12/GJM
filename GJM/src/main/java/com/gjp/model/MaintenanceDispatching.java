package com.gjp.model;

import java.util.Date;

/**
 * 派工单
 * 
 * @author zoe
 *
 */
public class MaintenanceDispatching {

	// 派工单编号
	private Integer mdg_id;
	// 内部人员编号
	private Integer em_id;
	// 维修申报编号
	private Integer md_id;
	// 派工时间
	private Date mdg_time;
	// 派单状态
	private String mdg_state;
	// 维修总金额
	private Double mdg_sumMoney;
	// 费用清单编码
	private String mdg_moneyCode;
	
	//特殊情况
	private String mdg_content;

	public MaintenanceDispatching() {
		super();
	}

	public Integer getMdg_id() {
		return mdg_id;
	}

	public void setMdg_id(Integer mdg_id) {
		this.mdg_id = mdg_id;
	}

	public Double getMdg_sumMoney() {
		return mdg_sumMoney;
	}

	public void setMdg_sumMoney(Double mdg_sumMoney) {
		this.mdg_sumMoney = mdg_sumMoney;
	}

	public String getMdg_moneyCode() {
		return mdg_moneyCode;
	}

	public void setMdg_moneyCode(String mdg_moneyCode) {
		this.mdg_moneyCode = mdg_moneyCode;
	}

	public Integer getEm_id() {
		return em_id;
	}

	public void setEm_id(Integer em_id) {
		this.em_id = em_id;
	}

	public String getMdg_content() {
		return mdg_content;
	}

	public void setMdg_content(String mdg_content) {
		this.mdg_content = mdg_content;
	}

	public Integer getMd_id() {
		return md_id;
	}

	public void setMd_id(Integer md_id) {
		this.md_id = md_id;
	}

	public Date getMdg_time() {
		return mdg_time;
	}

	public void setMdg_time(Date mdg_time) {
		this.mdg_time = mdg_time;
	}

	public String getMdg_state() {
		return mdg_state;
	}

	public void setMdg_state(String mdg_state) {
		this.mdg_state = mdg_state;
	}

}
