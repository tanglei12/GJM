package com.gjp.model;

import java.util.Date;
import java.util.List;

/**
 * 权限菜单表
 * @author 陈智颖
 *
 */
public class Powers {
	// 功能编码
	private Integer ucps_id;
	// 功能名称
	private String ucps_name;
	// 功能操作
	private String ucps_url;
	// 功能图标
	private String ucps_icon;
	// 功能父级
	private Integer ucps_pid;
	// 权限级别  
	private Integer ucps_level;
	// 功能排序
	private Integer ucps_asc;
	// 功能创建时间
	private Date ucps_date;
	// 功能状态
	private Integer ucps_state;
	// 1：查询权限 2：按钮权限 3:操作权限
	private Integer ucps_type;
	// 内部人员编码
	private Integer em_id;
	//子权限
	private List<Powers> childs;
	
	public Powers(){}

	public Integer getUcps_id() {
		return ucps_id;
	}

	public void setUcps_id(Integer ucps_id) {
		this.ucps_id = ucps_id;
	}

	public String getUcps_name() {
		return ucps_name;
	}

	public void setUcps_name(String ucps_name) {
		this.ucps_name = ucps_name;
	}

	public String getUcps_url() {
		return ucps_url;
	}

	public void setUcps_url(String ucps_url) {
		this.ucps_url = ucps_url;
	}

	public String getUcps_icon() {
		return ucps_icon;
	}

	public void setUcps_icon(String ucps_icon) {
		this.ucps_icon = ucps_icon;
	}

	public Integer getUcps_pid() {
		return ucps_pid;
	}

	public void setUcps_pid(Integer ucps_pid) {
		this.ucps_pid = ucps_pid;
	}

	public Integer getUcps_asc() {
		return ucps_asc;
	}

	public void setUcps_asc(Integer ucps_asc) {
		this.ucps_asc = ucps_asc;
	}

	public Date getUcps_date() {
		return ucps_date;
	}

	public void setUcps_date(Date ucps_date) {
		this.ucps_date = ucps_date;
	}

	public Integer getUcps_state() {
		return ucps_state;
	}

	public void setUcps_state(Integer ucps_state) {
		this.ucps_state = ucps_state;
	}

	public Integer getEm_id() {
		return em_id;
	}

	public void setEm_id(Integer em_id) {
		this.em_id = em_id;
	}

	public Integer getUcps_type() {
		return ucps_type;
	}

	public void setUcps_type(Integer ucps_type) {
		this.ucps_type = ucps_type;
	}

	public List<Powers> getChilds() {
		return childs;
	}

	public void setChilds(List<Powers> childs) {
		this.childs = childs;
	}

	public Integer getUcps_level() {
		return ucps_level;
	}

	public void setUcps_level(Integer ucps_level) {
		this.ucps_level = ucps_level;
	}

}
