package com.gjp.model;

import java.util.Date;

/**
 * 业绩记录
 * 
 * @author 陈智颖
 *
 * @version 创建时间：2016年9月22日 下午4:08:57
 */
public class AchievementRecord {

	// 业绩记录ID
	private Integer ar_id;
	// 合同ID
	private Integer contractObject_Id;
	// 总业绩ID
	private Integer sa_id;
	// 记录内容
	private String ar_content;
	// 时间
	private Date ar_date;
	// 内部人员
	private Integer em_id;
	// '重新生成','业绩调整'
	private String ar_type;
	// 调整金额
	private double ar_money;

	// ----------扩展-----------
	// 管家名称
	private String em_name;

	public Integer getAr_id() {
		return ar_id;
	}

	public void setAr_id(Integer ar_id) {
		this.ar_id = ar_id;
	}

	public String getAr_content() {
		return ar_content;
	}

	public void setAr_content(String ar_content) {
		this.ar_content = ar_content;
	}

	public Date getAr_date() {
		return ar_date;
	}

	public void setAr_date(Date ar_date) {
		this.ar_date = ar_date;
	}

	public Integer getEm_id() {
		return em_id;
	}

	public void setEm_id(Integer em_id) {
		this.em_id = em_id;
	}

	public String getAr_type() {
		return ar_type;
	}

	public void setAr_type(String ar_type) {
		this.ar_type = ar_type;
	}

	public double getAr_money() {
		return ar_money;
	}

	public void setAr_money(double ar_money) {
		this.ar_money = ar_money;
	}

	public Integer getContractObject_Id() {
		return contractObject_Id;
	}

	public void setContractObject_Id(Integer contractObject_Id) {
		this.contractObject_Id = contractObject_Id;
	}

	public String getEm_name() {
		return em_name;
	}

	public void setEm_name(String em_name) {
		this.em_name = em_name;
	}

	public Integer getSa_id() {
		return sa_id;
	}

	public void setSa_id(Integer sa_id) {
		this.sa_id = sa_id;
	}

}
