package com.gjp.model;

import java.util.Date;

/**
 * @author 陈智颖
 *
 * @version 创建时间：2016年3月4日 上午10:36:59 
 */
public class Settings {

	//统计设置编码
	private Integer css_id;
	//统计设置类型
	private Integer css_type;
	//统计设置个数
	private Integer css_num;
	//统计设置时间
	private Date css_time;
	
	public Settings(){}

	public Integer getCss_id() {
		return css_id;
	}

	public void setCss_id(Integer css_id) {
		this.css_id = css_id;
	}

	public Integer getCss_type() {
		return css_type;
	}

	public void setCss_type(Integer css_type) {
		this.css_type = css_type;
	}

	public Integer getCss_num() {
		return css_num;
	}

	public void setCss_num(Integer css_num) {
		this.css_num = css_num;
	}

	public Date getCss_time() {
		return css_time;
	}

	public void setCss_time(Date css_time) {
		this.css_time = css_time;
	}
	
}
