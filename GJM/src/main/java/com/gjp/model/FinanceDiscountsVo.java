package com.gjp.model;

import java.util.Date;

/**
 * 支付优惠、抵扣等
 *
 * @author 申洪喜
 * @create 2017-08-23 16:00
 **/
public class FinanceDiscountsVo {

    // 主键ID
    private Integer fd_id;
    // 支付流水号
    private String  bs_serialNumber;
    // 抵扣类型 1-包修费；2-服务费；3-优惠券；4-折扣；5-其他
    private Integer discount_type;
    // 抵扣金额
    private double discount_money;
    // 录入时间
    private Date create_time;

    public Integer getFd_id() {
        return fd_id;
    }

    public void setFd_id(Integer fd_id) {
        this.fd_id = fd_id;
    }

    public String getBs_serialNumber() {
        return bs_serialNumber;
    }

    public void setBs_serialNumber(String bs_serialNumber) {
        this.bs_serialNumber = bs_serialNumber;
    }

    public Integer getDiscount_type() {
        return discount_type;
    }

    public void setDiscount_type(Integer discount_type) {
        this.discount_type = discount_type;
    }

    public double getDiscount_money() {
        return discount_money;
    }

    public void setDiscount_money(double discount_money) {
        this.discount_money = discount_money;
    }

    public Date getCreate_time() {
        return create_time;
    }

    public void setCreate_time(Date create_time) {
        this.create_time = create_time;
    }
}
