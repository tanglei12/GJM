package com.gjp.model;


/**
 * 测评内容
 * 
 * @author zoe
 *
 */
public class UserCenterEvaluationContent {

	// 测评编号
	private Integer ec_id;
	// 测评标题
	private String ec_title;
	// 测评内容
	private String ec_content;
	// 测评人编码
	private Integer ep_id;

	public UserCenterEvaluationContent() {
		super();
	}

	public Integer getEc_id() {
		return ec_id;
	}

	public void setEc_id(Integer ec_id) {
		this.ec_id = ec_id;
	}

	public String getEc_title() {
		return ec_title;
	}

	public void setEc_title(String ec_title) {
		this.ec_title = ec_title;
	}

	public String getEc_content() {
		return ec_content;
	}

	public void setEc_content(String ec_content) {
		this.ec_content = ec_content;
	}

	public Integer getEp_id() {
		return ep_id;
	}

	public void setEp_id(Integer ep_id) {
		this.ep_id = ep_id;
	}

	
}
