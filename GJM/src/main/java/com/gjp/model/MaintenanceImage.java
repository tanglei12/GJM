package com.gjp.model;


/**
 * 申请图片
 * 
 * @author zoe
 *
 */
public class MaintenanceImage {

	// 申报图片编码
	private Integer mi_id;
	// 申报图片名
	private String mi_name;
	// 申报图片路径
	private String mi_path;
	// 维修申报编码
	private Integer md_id;
	// 图片类型
	private String mi_type;
	// 圖片展示
	private String img_path;

	public MaintenanceImage() {
		super();
	}

	public Integer getMi_id() {
		return mi_id;
	}

	public void setMi_id(Integer mi_id) {
		this.mi_id = mi_id;
	}

	public String getMi_name() {
		return mi_name;
	}

	public void setMi_name(String mi_name) {
		this.mi_name = mi_name;
	}

	public String getMi_path() {
		return mi_path;
	}

	public void setMi_path(String mi_path) {
		this.mi_path = mi_path;
	}

	public Integer getMd_id() {
		return md_id;
	}

	public void setMd_id(Integer md_id) {
		this.md_id = md_id;
	}

	public String getMi_type() {
		return mi_type;
	}

	public void setMi_type(String mi_type) {
		this.mi_type = mi_type;
	}

	public String getImg_path() {
		return img_path;
	}

	public void setImg_path(String img_path) {
		this.img_path = img_path;
	}
}
