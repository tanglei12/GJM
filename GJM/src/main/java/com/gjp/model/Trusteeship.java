package com.gjp.model;

import java.util.Date;


/**
 * 房屋托管基本信息
 * 
 * @author zoe
 *
 */
public class Trusteeship {

	// 托管提交ID
	private Integer ts_id;
	// 房屋托管人姓名
	private String ts_name;
	// 房屋托管人电话
	private String ts_phone;
	// '几房',
	private String ts_houseT;
	// '房屋类型 清水房 精装 简装',
	private String ts_houseType;
	// 地址
	private String ts_address;
	// 时间
	private Date ts_time;
	// 托管处理状态
	private String ts_state;
	// 备注
	private String ts_txt;
	
	public Trusteeship() {
		super();
	}

	public Integer getTs_id() {
		return ts_id;
	}

	public void setTs_id(Integer ts_id) {
		this.ts_id = ts_id;
	}

	public String getTs_name() {
		return ts_name;
	}

	public void setTs_name(String ts_name) {
		this.ts_name = ts_name;
	}

	public String getTs_phone() {
		return ts_phone;
	}

	public void setTs_phone(String ts_phone) {
		this.ts_phone = ts_phone;
	}

	public String getTs_houseT() {
		return ts_houseT;
	}

	public void setTs_houseT(String ts_houseT) {
		this.ts_houseT = ts_houseT;
	}

	public String getTs_houseType() {
		return ts_houseType;
	}

	public void setTs_houseType(String ts_houseType) {
		this.ts_houseType = ts_houseType;
	}

	public String getTs_address() {
		return ts_address;
	}

	public void setTs_address(String ts_address) {
		this.ts_address = ts_address;
	}

	public Date getTs_time() {
		return ts_time;
	}

	public void setTs_time(Date ts_time) {
		this.ts_time = ts_time;
	}

	public String getTs_state() {
		return ts_state;
	}

	public void setTs_state(String ts_state) {
		this.ts_state = ts_state;
	}

	public String getTs_txt() {
		return ts_txt;
	}

	public void setTs_txt(String ts_txt) {
		this.ts_txt = ts_txt;
	}

	


}
