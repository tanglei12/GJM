package com.gjp.model;

import java.util.Date;

/**
 * 服务外协
 *
 * @author shenhx
 * @create 2017-08-26 11:41
 **/
public class ServiceOutsource {

    // 主键ID
    private Integer so_id;
    // 外协名称
    private String outsource_name;
    // 外协报价
    private double outsource_price;
    // 外协电话
    private String outsource_phone;
    // 处理结果1-成功；2-失败
    private Integer outsource_result;
    // 结束时间
    private Date end_time;
    // 申请时间
    private Date create_time;
    // 服务订单
    private Integer md_id;
    // 联系管家
    private Integer em_id;

    public Integer getSo_id() {
        return so_id;
    }

    public void setSo_id(Integer so_id) {
        this.so_id = so_id;
    }

    public String getOutsource_name() {
        return outsource_name;
    }

    public void setOutsource_name(String outsource_name) {
        this.outsource_name = outsource_name;
    }

    public double getOutsource_price() {
        return outsource_price;
    }

    public void setOutsource_price(double outsource_price) {
        this.outsource_price = outsource_price;
    }

    public String getOutsource_phone() {
        return outsource_phone;
    }

    public void setOutsource_phone(String outsource_phone) {
        this.outsource_phone = outsource_phone;
    }

    public Integer getOutsource_result() {
        return outsource_result;
    }

    public void setOutsource_result(Integer outsource_result) {
        this.outsource_result = outsource_result;
    }

    public Date getEnd_time() {
        return end_time;
    }

    public void setEnd_time(Date end_time) {
        this.end_time = end_time;
    }

    public Date getCreate_time() {
        return create_time;
    }

    public void setCreate_time(Date create_time) {
        this.create_time = create_time;
    }

    public Integer getMd_id() {
        return md_id;
    }

    public void setMd_id(Integer md_id) {
        this.md_id = md_id;
    }

    public Integer getEm_id() {
        return em_id;
    }

    public void setEm_id(Integer em_id) {
        this.em_id = em_id;
    }
}
