package com.gjp.model;

import java.util.Date;

/**
 * 合同服务费对象
 *
 * @author JiangQt
 * @createTime 2015年12月11日下午4:31:53
 */
public class ServiceContractServiceChargeVo {

	private Integer csc_id;// 合同服务费编号
	private String contractObject_no;// 合同编号
	private String hi_code;// 房屋编号
	private double csc_money;// 服务费
	private String guaranteeCost;// 包修费
	private Date csc_createTime;// 创建时间

	public Integer getCsc_id() {
		return csc_id;
	}

	public void setCsc_id(Integer csc_id) {
		this.csc_id = csc_id;
	}

	public String getContractObject_no() {
		return contractObject_no;
	}

	public void setContractObject_no(String contractObject_no) {
		this.contractObject_no = contractObject_no;
	}

	public String getHi_code() {
		return hi_code;
	}

	public void setHi_code(String hi_code) {
		this.hi_code = hi_code;
	}

	public double getCsc_money() {
		return csc_money;
	}

	public void setCsc_money(double csc_money) {
		this.csc_money = csc_money;
	}

	public Date getCsc_createTime() {
		return csc_createTime;
	}

	public void setCsc_createTime(Date csc_createTime) {
		this.csc_createTime = csc_createTime;
	}

	public String getGuaranteeCost() {
		return guaranteeCost;
	}

	public void setGuaranteeCost(String guaranteeCost) {
		this.guaranteeCost = guaranteeCost;
	}
}
