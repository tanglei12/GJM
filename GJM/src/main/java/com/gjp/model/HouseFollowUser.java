package com.gjp.model;

/**
 * 跟进人员表
 * 
 * @author zoe
 *
 */
public class HouseFollowUser {

	// 跟进人员编号
	private Integer ghf_id;
	// 意向房源编号
	private Integer phi_id;
	// 内部人员编号
	private Integer em_id;
	// 跟进状态
	private String ghf_state;

	public HouseFollowUser() {
		super();
	}

	public Integer getGhf_id() {
		return ghf_id;
	}

	public void setGhf_id(Integer ghf_id) {
		this.ghf_id = ghf_id;
	}

	public Integer getPhi_id() {
		return phi_id;
	}

	public void setPhi_id(Integer phi_id) {
		this.phi_id = phi_id;
	}

	public Integer getEm_id() {
		return em_id;
	}

	public void setEm_id(Integer em_id) {
		this.em_id = em_id;
	}

	public String getGhf_state() {
		return ghf_state;
	}

	public void setGhf_state(String ghf_state) {
		this.ghf_state = ghf_state;
	}


}
