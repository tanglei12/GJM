package com.gjp.model;

import java.util.Date;

/**
 * 短信模板
 * 
 * @author 陈智颖
 *
 * @version 创建时间：2016年8月15日 下午5:38:40 
 */
public class UserCenterMessageModel {

	// 短信模板编码
	private Integer mm_id;
	// 模板题目
	private String mm_name;
	// 模板内容
	private String mm_text;
	// 模板类型
	private Integer mm_type;
	// 模板创建时间
	private Date mm_date;
	// 模板创建人
	private Integer em_id;
	//用户名
	private String em_name;
	
	public Integer getMm_id() {
		return mm_id;
	}
	public void setMm_id(Integer mm_id) {
		this.mm_id = mm_id;
	}
	public String getMm_text() {
		return mm_text;
	}
	public void setMm_text(String mm_text) {
		this.mm_text = mm_text;
	}
	public Integer getMm_type() {
		return mm_type;
	}
	public void setMm_type(Integer mm_type) {
		this.mm_type = mm_type;
	}
	public Date getMm_date() {
		return mm_date;
	}
	public void setMm_date(Date mm_date) {
		this.mm_date = mm_date;
	}
	public Integer getEm_id() {
		return em_id;
	}
	public void setEm_id(Integer em_id) {
		this.em_id = em_id;
	}
	public String getMm_name() {
		return mm_name;
	}
	public void setMm_name(String mm_name) {
		this.mm_name = mm_name;
	}
	public String getEm_name() {
		return em_name;
	}
	public void setEm_name(String em_name) {
		this.em_name = em_name;
	}
	
}
